void drawCursor(char *vram, int xsize, int x, int y, char cursor_style[16][16]){
  for(int j = 0; j < 16; j++){
    for(int i=0; i<16; i++){
      if(cursor_style[j][i] == 'o'){
        vram[(y+j)*xsize + (x+i)] = 0; // 中心是黑色 
      }
      if(cursor_style[j][i] == '*'){
        vram[(y+j)*xsize + (x+i)] = 15; // 边框是白色 
      }
    }
  }
}


